public class ProximoEventoModel {
    private String nomeBanda;
    private String qtdReserva;
    private String dtEvento;
    private String cdevento;


    public String getNomeBanda() {
        return nomeBanda;
    }

    public void setNomeBanda(String nomeBanda) {
        this.nomeBanda = nomeBanda;
    }

    public String getQtdReserva() {
        return qtdReserva;
    }

    public void setQtdReserva(String qtdReserva) {
        this.qtdReserva = qtdReserva;
    }

    public String getDtEvento() {
        return dtEvento;
    }

    public void setDtEvento(String dtEvento) {
        this.dtEvento = dtEvento;
    }

    public String getCdevento() {
        return cdevento;
    }

    public void setCdevento(String cdevento) {
        this.cdevento = cdevento;
    }
}
